n = int(input())
if n == 1:
    print('*')
else:
    print('*'*n + ' '*(2*n-3) + '*'*n)
    for i in range(2, n):
        print(' ' * (i -1) + '*' + ' '*(n-2) + '*' + ' '*(2*n-1-2*i) + '*' + ' '*(n-2) + '*')
    print(' ' * (n-1) + '*' + ' '*(n-2) + '*' + ' '*(n-2) + '*')

    for i in range(2, n):
        print(' ' * (n-i) + '*' + ' '*(n-2) + '*' + ' '*(2*i-3) + '*' + ' '*(n-2) + '*')
    print('*'*n + ' '*(2*n-3) + '*'*n)